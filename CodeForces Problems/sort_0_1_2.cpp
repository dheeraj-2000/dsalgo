void sort012(int *arr, int n)
{
   for(int i=0;i<n;i++)
   {
       for(int j=0;j<n;j++)
       {
           if(arr[j]<=arr[j+1]);
           else
               swap(arr[j], arr[j + 1]);
       }
   }
}
